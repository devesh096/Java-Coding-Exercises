import java.util.Scanner;
class sum
{
	public static void main(String[] args)
	{
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter a Natural Number to Get a Sum");
		int N=sc.nextInt();
		int s=0,i;
		for(i=1;i<=N;i++)
		{
			s=s+2*i;
		}
		System.out.println("Sum is = "+s);
		
		
		
		
		
		
		
		
		
		
	}
	
	
	
}